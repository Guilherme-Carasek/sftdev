package Ex_06;

public class Atleta {
    private String nome;
    private String modalidade;
    private int alturaCm;
    private double pesoKg;
    private String paisOrigem;

    public Atleta(String nome, String modalidade, int alturaCm, double pesoKg, String paisOrigem) {
        this.nome = nome;
        this.modalidade = modalidade;
        this.alturaCm = alturaCm;
        this.pesoKg = pesoKg;
        this.paisOrigem = paisOrigem;
    }
}
